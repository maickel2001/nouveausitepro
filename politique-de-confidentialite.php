document.addEventListener('DOMContentLoaded', function() {
    // Animation des sections au scroll
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate');
            }
        });
    });
    
    const animatedElements = document.querySelectorAll('.privacy-section, .action-card, .cta-content');
    animatedElements.forEach(el => observer.observe(el));
    
    // Navigation breadcrumb
    const breadcrumb = document.createElement('nav');
    breadcrumb.className = 'breadcrumb';
    breadcrumb.innerHTML = `
        <ol>
            <li><a href="/">Accueil</a></li>
            <li aria-current="page">Politique de confidentialité</li>
        </ol>
    `;
    
    const pageHeader = document.querySelector('.section-header');
    if (pageHeader) {
        pageHeader.parentNode.insertBefore(breadcrumb, pageHeader);
    }
    
    // Bouton retour en haut
    const backToTopBtn = document.createElement('button');
    backToTopBtn.className = 'back-to-top';
    backToTopBtn.innerHTML = '<i class="fas fa-arrow-up"></i>';
    backToTopBtn.setAttribute('aria-label', 'Retour en haut');
    
    document.body.appendChild(backToTopBtn);
    
    backToTopBtn.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });
    
    // Affichage/masquage du bouton retour en haut
    window.addEventListener('scroll', () => {
        if (window.pageYOffset > 300) {
            backToTopBtn.classList.add('visible');
        } else {
            backToTopBtn.classList.remove('visible');
        }
    });
    
    // Animation des cartes d'action au survol
    const actionCards = document.querySelectorAll('.action-card');
    actionCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-5px)';
            this.style.boxShadow = '0 10px 25px rgba(0, 0, 0, 0.15)';
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0)';
            this.style.boxShadow = '0 5px 15px rgba(0, 0, 0, 0.1)';
        });
    });
    
    // Mise en surbrillance des liens internes
    const internalLinks = document.querySelectorAll('a[href^="/"]');
    internalLinks.forEach(link => {
        link.addEventListener('mouseenter', function() {
            this.style.color = '#2563eb';
            this.style.textDecoration = 'underline';
        });
        
        link.addEventListener('mouseleave', function() {
            this.style.color = '';
            this.style.textDecoration = '';
        });
    });
    
    // Table des matières automatique
    const createTableOfContents = () => {
        const headings = document.querySelectorAll('.privacy-section h2');
        if (headings.length > 0) {
            const toc = document.createElement('div');
            toc.className = 'table-of-contents';
            toc.innerHTML = '<h3>Table des matières</h3><ul></ul>';
            
            const tocList = toc.querySelector('ul');
            headings.forEach((heading, index) => {
                const li = document.createElement('li');
                const a = document.createElement('a');
                a.href = `#section-${index + 1}`;
                a.textContent = heading.textContent;
                a.addEventListener('click', (e) => {
                    e.preventDefault();
                    heading.scrollIntoView({ behavior: 'smooth' });
                });
                li.appendChild(a);
                tocList.appendChild(li);
                
                // Ajouter un ID à la section
                heading.parentElement.id = `section-${index + 1}`;
            });
            
            // Insérer la table des matières après le header
            const sectionHeader = document.querySelector('.section-header');
            if (sectionHeader) {
                sectionHeader.parentNode.insertBefore(toc, sectionHeader.nextSibling);
            }
        }
    };
    
    createTableOfContents();
});
</script>

